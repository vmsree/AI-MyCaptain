s1 = {1,2,3,4}
s2 = {2,4,7,8,9}

Union = s1.union(s2)
Difference = s1.difference(s2)
Intersection = s1.intersection(s2)
Symmetric_difference = s1.symmetric_difference(s2)

print("Union of sets: ", Union)
print("Difference of sets: ", Difference)
print("Intersection of sets: ", Intersection)
print("Symmetric_difference of sets: ", Symmetric_difference)
